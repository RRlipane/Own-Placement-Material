#include<stdio.h>
//loops in c
int main(){

    printf("\nFor loop execution");
   //for loop
   for(int i=0;i<5;i++){
    printf("\nhello");
   }

   printf("while loop execution\n");

   //while loop

   int i=0;
   while(i<5){
    printf("\nhello");
    i++;
   }

   printf("do-while execution\n");

   //do-while
    int j=0;
   do{
    printf("\nhello");
    j++;
   }while(j<5);
}

